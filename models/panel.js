const mongoose = require('mongoose');

const panelSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    contact: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    }
})

const panels = mongoose.model('panels', panelSchema);

module.exports = panels;